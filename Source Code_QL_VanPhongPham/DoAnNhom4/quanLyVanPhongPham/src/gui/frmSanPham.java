    public  void load(){
        model.setRowCount(0);
        ArrayList<SanPham> dssp= SanPhamDAO.layDSSP();
        for(SanPham sp:dssp){
            Vector<Object> row= new Vector<Object>();
            row.add(sp.getMaSP());
            row.add(sp.getTenSP());
            row.add(sp.getMaLoai());
            row.add(sp.getDonGua());
            row.add(sp.getSoLuongTon());
            model.addRow(row);
        }
    }
    public void loadLoaiSP(){
            DSML=LoaiSPDAO.layDSLSP();
            for(LoaiSP Lsp:DSML){
            cboLoaiSP.addItem(Lsp.getTenLoai());
        }
    }
