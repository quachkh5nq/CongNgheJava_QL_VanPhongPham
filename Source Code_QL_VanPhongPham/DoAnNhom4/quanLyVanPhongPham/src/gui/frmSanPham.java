    private void tbSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbSanPhamMouseClicked
        // TODO add your handling code here:
        int i= tbSanPham.getSelectedRow();
        txtMaSP.setText(String.valueOf(tbSanPham.getValueAt(i,0)));
        txtTenSP.setText(String.valueOf(tbSanPham.getValueAt(i,1)));
        txtDonGia.setText(String.valueOf(tbSanPham.getValueAt(i,3)));
        txtsoluong.setText(String.valueOf(tbSanPham.getValueAt(i,4)));
        int mal=Integer.parseInt(tbSanPham.getValueAt(i, 2).toString());
        for(LoaiSP rs:DSML){
            if(mal==rs.getMaLoai()){
                cboLoaiSP.setSelectedItem(rs.getTenLoai());
            }
        
        }
    }//GEN-LAST:event_tbSanPhamMouseClicked

